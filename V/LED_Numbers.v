import os

fn main(){
	leds := {
		'0': [" _  ","| | ","|_| "],
		'1': ["  ","| ","| "],
		'2': [" _  "," _| ","|_  "],
		'3': ["_  ","_| ","_| "],
		'4': ["    ","|_| ","  | "],
		'5': [" _  ","|_  "," _| "],
		'6': [" _  ","|_  ","|_| "],
		'7': ["_   "," |  "," |  "],
		'8': [" _  ","|_| ","|_| "],
		'9': [" _  ","|_| "," _| "]
	}
	
	print("Enter a number: ")
	number := os.get_line()
	for i := 0; i < 3; i++{
		for num in number {
			print(leds[num.str()][i])
		}
		println("")
	}			
}
